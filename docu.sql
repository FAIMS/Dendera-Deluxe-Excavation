select response as 'All Descriptions' from (
select aenttypeid, 0 as attributeid, 0 as vocabid, '# '||aenttypename||'
*'|| coalesce(nullif(aenttypedescription,''),'No Description.') ||'*' as response from aenttype
union
select aenttypeid, attributeid, 0, '  * '||attributename||': '|| attributedescription from attributekey join idealaent using (attributeid) join aenttype using (aenttypeid)
union
select aenttypeid, attributeid, vocabid, '    * '||coalesce(val, vocabname)||' *'||vocabname||'*: '|| COALESCE(vocabdescription,'No Description') from vocabulary left outer join keyval on (vocabname = '{'||key||'}') join attributekey using (attributeid) join idealaent using (attributeid) join aenttype using (aenttypeid)
)
order by aenttypeid, attributeid, vocabid;
